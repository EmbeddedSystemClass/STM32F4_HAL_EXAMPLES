/* USER CODE BEGIN 0 */
#define ONE_ROTATION_PULSES 6
uint16_t cnt1, cnt2, diff, speed, dir;
uint32_t tick;

/* USER CODE END 0 */

/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL);
  HAL_TIM_OC_Start(&htim4, TIM_CHANNEL_1);
  HAL_TIM_OC_Start(&htim4, TIM_CHANNEL_2);


  /* 현재 카운터와 Tick 값 */
  cnt1 = TIM3->CNT;
  tick = HAL_GetTick();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    if(HAL_GetTick() - tick > 1000L)
    {
      /* 회전 방향 확인 */
      if(__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim3))
      {
        dir = 1;
      }
      else
      {
        dir = 0;
      }

      /* 1초마다 TIM3 카운터 증가 확인 */
      cnt2 = TIM3->CNT;

      if(dir)
      {
        /* Down Counting 방향으로 회전할 때 */
        if(cnt1 > cnt2) diff = cnt1 - cnt2;
        else diff = (TIM3->ARR + cnt1) - cnt2 ;
      }
      else
      {
        /* Up Counting 방향으로 회전할 때 */
        if(cnt2 > cnt1) diff = cnt2 - cnt1;
        else diff = (TIM3->ARR + cnt2) - cnt1 ;
      }

      if((TIM3->SMCR & 0x03) == 0x03)
      {
        /* X4 Mode 일때는 카운터가 4 증가할 때, 1개의 Pulse */
        speed = diff * 60 / 4 / ONE_ROTATION_PULSES; //RPM 계산을 위해서는 x 60
      }
      else
      {
        /* X4 Mode 일때는 카운터가 2 증가할 때, 1개의 Pulse */
        speed = diff * 60 / 2 / ONE_ROTATION_PULSES; //RPM 계산을 위해서는 x 60
      }

      tick = HAL_GetTick();
      cnt1 = TIM3->CNT;
    }
  }
  /* USER CODE END 3 */

}

